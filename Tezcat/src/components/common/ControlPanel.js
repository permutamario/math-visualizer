import React, { useState } from "react";
import { useTheme } from "../../contexts/ThemeContext";

export const ControlPanel = ({
  title,
  children,
  className = "",
  panelType = "",
}) => {
  const { darkMode } = useTheme();
  const [collapsed, setCollapsed] = useState(false);

  // Determine which direction to collapse and panel characteristics
  const isRightSide =
    className.includes("right") ||
    panelType === "structural" ||
    panelType === "class";

  // Calculate transform for collapsed state
  const transformValue = isRightSide
    ? collapsed
      ? "translateX(calc(100% - 30px))"
      : "translateX(0)"
    : collapsed
    ? "translateX(calc(-100% + 30px))"
    : "translateX(0)";

  return (
    <div
      className={`bg-white dark:bg-gray-700 rounded-lg shadow-lg z-10 ${className}`}
      style={{
        transition: "transform 0.3s ease-in-out",
        transform: transformValue,
        overflow: "hidden",
      }}
    >
      <div className="p-3 relative">
        {/* Panel Icon Indicator - Clickable */}
        <div
          className="absolute top-3 cursor-pointer w-7 h-7 flex items-center justify-center bg-blue-500 text-white rounded-md text-sm font-bold"
          style={{
            [isRightSide ? "left" : "right"]: "12px",
            zIndex: 2,
          }}
          onClick={() => setCollapsed(!collapsed)}
        >
          {panelType === "visual" && (
            <svg
              xmlns="http://www.w3.org/2000/svg"
              viewBox="0 0 24 24"
              fill="currentColor"
              className="w-4 h-4"
            >
              <path d="M12 15a3 3 0 100-6 3 3 0 000 6z" />
              <path
                fillRule="evenodd"
                d="M1.323 11.447C2.811 6.976 7.028 3.75 12.001 3.75c4.97 0 9.185 3.223 10.675 7.69.12.362.12.752 0 1.113-1.487 4.471-5.705 7.697-10.677 7.697-4.97 0-9.186-3.223-10.675-7.69a1.762 1.762 0 010-1.113zM17.25 12a5.25 5.25 0 11-10.5 0 5.25 5.25 0 0110.5 0z"
                clipRule="evenodd"
              />
            </svg>
          )}
          {panelType === "structural" && (
            <svg
              xmlns="http://www.w3.org/2000/svg"
              viewBox="0 0 24 24"
              fill="currentColor"
              className="w-4 h-4"
            >
              <path
                fillRule="evenodd"
                d="M11.078 2.25c-.917 0-1.699.663-1.85 1.567L9.05 4.889c-.02.12-.115.26-.297.348a7.493 7.493 0 00-.986.57c-.166.115-.334.126-.45.083L6.3 5.508a1.875 1.875 0 00-2.282.819l-.922 1.597a1.875 1.875 0 00.432 2.385l.84.692c.095.078.17.229.154.43a7.598 7.598 0 000 1.139c.015.2-.059.352-.153.43l-.841.692a1.875 1.875 0 00-.432 2.385l.922 1.597a1.875 1.875 0 002.282.818l1.019-.382c.115-.043.283-.031.45.082.312.214.641.405.985.57.182.088.277.228.297.35l.178 1.071c.151.904.933 1.567 1.85 1.567h1.844c.916 0 1.699-.663 1.85-1.567l.178-1.072c.02-.12.114-.26.297-.349.344-.165.673-.356.985-.57.167-.114.335-.125.45-.082l1.02.382a1.875 1.875 0 002.28-.819l.923-1.597a1.875 1.875 0 00-.432-2.385l-.84-.692c-.095-.078-.17-.229-.154-.43a7.614 7.614 0 000-1.139c-.016-.2.059-.352.153-.43l.84-.692c.708-.582.891-1.59.433-2.385l-.922-1.597a1.875 1.875 0 00-2.282-.818l-1.02.382c-.114.043-.282.031-.449-.083a7.49 7.49 0 00-.985-.57c-.183-.087-.277-.227-.297-.348l-.179-1.072a1.875 1.875 0 00-1.85-1.567h-1.843zM12 15.75a3.75 3.75 0 100-7.5 3.75 3.75 0 000 7.5z"
                clipRule="evenodd"
              />
            </svg>
          )}
          {panelType === "actions" && (
            <svg
              xmlns="http://www.w3.org/2000/svg"
              viewBox="0 0 24 24"
              fill="currentColor"
              className="w-4 h-4"
            >
              <path
                fillRule="evenodd"
                d="M14.615 1.595a.75.75 0 01.359.852L12.982 9.75h7.268a.75.75 0 01.548 1.262l-10.5 11.25a.75.75 0 01-1.272-.71l1.992-7.302H3.75a.75.75 0 01-.548-1.262l10.5-11.25a.75.75 0 01.913-.143z"
                clipRule="evenodd"
              />
            </svg>
          )}
          {panelType === "class" && (
            <svg
              xmlns="http://www.w3.org/2000/svg"
              viewBox="0 0 24 24"
              fill="currentColor"
              className="w-4 h-4"
            >
              <path
                fillRule="evenodd"
                d="M11.47 4.72a.75.75 0 011.06 0l3.75 3.75a.75.75 0 01-1.06 1.06L12 6.31 8.78 9.53a.75.75 0 01-1.06-1.06l3.75-3.75zm-3.75 9.75a.75.75 0 011.06 0L12 17.69l3.22-3.22a.75.75 0 111.06 1.06l-3.75 3.75a.75.75 0 01-1.06 0l-3.75-3.75a.75.75 0 010-1.06z"
                clipRule="evenodd"
              />
            </svg>
          )}
        </div>

        {/* Panel Title */}
        <h3
          className="text-lg font-semibold text-gray-800 dark:text-white pb-1 mb-1 border-b border-gray-200 dark:border-gray-600 cursor-pointer text-center"
          onClick={() => setCollapsed(!collapsed)}
        >
          {title}
        </h3>

        {/* Panel Content */}
        <div className={collapsed ? "hidden" : "mt-2"}>{children}</div>
      </div>
    </div>
  );
};

export default ControlPanel;
